<p>「那就黃昏見啦，我可以趁這時候去側街東南角哪兒逛逛...」</p>

<p>「喂，你……」芬斯特藍半認真地調侃道。</p>

<p>「喂，我沒擋你探險，你也別擋我『探險』。哼，你自命清高，看來更有可能一輩子當光棍呢。」瑞福做了個鬼臉，跑掉了。</p>

<p>「這就是王都的『年輕人』，唉，」芬斯特藍苦笑著搖搖頭，「我還是比較喜歡山上的探險家。」
