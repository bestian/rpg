<h3>1.1.2. 芬斯特藍</h3>
<p>洛南紀五十九年十月十九日，王都諾薩里亞</p>

<p>「欸！芬斯特藍！你聽說那個預言了嗎？」一個穿著講究、面目清秀的年輕人說道。</p>

<p>「什麼預言？」芬斯特藍躺在藝文廣場的草地上，看著初冬午後清朗的天空，隨口應著。</p>

<p>芬斯特藍，二十一歲，薩斯坦提斯男爵的長子。他有著金色垂肩的長髮、一雙出外探險練出的結實的臂膀、優雅迷人的吟唱聲調。</p>

<p>他有過不少浪漫故事，卻從不提及婚姻。有人猜測，雖然到了適婚年齡，芬斯特藍．薩斯坦提斯仍然嚮往冒險。</p>

<p>「那個皇家森林裡的隱者講的，」芬斯特藍身旁的年輕人續道，「你也知道，他一年難得開一句金口，上禮拜啊，一說就是個可怕的預言欸。」</p>

<p>「哦，所以呢？」芬斯特藍不太在意，繼續享受千草輕扎的感覺。</p>

<p>「所以？」年輕人加大音量，「所以今晚就特別重要啦！陛下已經下令，如果隱者的預言落空，他在皇家森林的茅屋就要拆除！你知道很多人都討厭那個神秘兮兮的隱者。」</p>

<p>「我看要趕他走也沒那麼容易，畢竟他是萬物和諧宗的最高隱者。等等，他到底預言了什麼？」</p>

<p>萬物和諧宗，並不是一個真正的教派，是吟遊詩人自己為隱者取的。隱者們崇尚自然，過者非常簡樸的生活，有的隱者甚至不和他人同住，像皇家隱者就是獨居。</p>

