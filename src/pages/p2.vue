<p>「套句探險家的話，只要心還在跳，什麼事都是可能的。」芬斯特藍笑道，「那好，咱們就約黃昏在西牆上碰頭，一起看看那預言會不會成真吧。」</p>

<p>「怎麼，你待會兒有事？」</p>

<p>「是的，瑞福，」芬斯特藍坐直了身子，嚴肅地說道：「我要去一趟古董店。最近那兒來了奇怪的人，可能是考古學家，買了許多不相干的東西，處處透著詭秘。而且，他說他今天還會再去。」</p>

<p>「你怎麼知道這消息？」年輕人瑞福問道。</p>

<p>「這些都是老闆告訴我的。」芬斯特藍笑道。</p>

<p>「你跟老闆熟？」</p>

<p>「喂，別忘了我也到過西方冒險。我們以前是同伴啊。」</p>

